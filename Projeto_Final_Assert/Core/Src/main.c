/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MPU Configuration--------------------------------------------------------*/
  MPU_Config();

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_ADC1_Init();
  MX_ETH_Init();
  MX_USART3_UART_Init();
  MX_USB_OTG_FS_PCD_Init();
  MX_TIM6_Init();
  MX_TIM3_Init();
  MX_TIM7_Init();
  /* USER CODE BEGIN 2 */
  // Agora sim! O hardware principal esta ligado. Podemos iniciar os modulos:
  Bsp_Init();
  Sampler_Init();
  LedPwm_Init();
  SerialCmd_Init();
  Button_Init();
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
    // Processa de forma continua as entradas de interface (Nao bloqueantes)
    Button_Process();
    SerialCmd_Process();

    // 3.1. Verificacao do gatilho do temporizador de amostragem (A cada 5 ms)
    if (Bsp_GetSamplingFlag() == true)
    {
        Bsp_ClearSamplingFlag();

        // Incrementa a contagem de tempo para a telemetria de 1 segundo
        tickCounter++;

        // REQUISITO 3.5: Se o botao congelou a operacao, pula a leitura e atuacao
        if (Button_IsSystemFrozen() == false)
        {
            // Executa a leitura do ADC por Polling no laco principal (Requisito 3.1)
            uint16_t rawAdc = Bsp_ReadAdc();

            // Envia a amostra bruta para ser acumulada no filtro de media (Requisito 3.2)
            Sampler_ProcessNewSample(rawAdc);
        }

        // 3.2. Verifica se a janela de filtragem (100 amostras / 500 ms) foi concluida
        if (Sampler_IsNewAverageReady() == true)
        {
            Sampler_ClearAverageFlag();

            // Recupera a media convertida para 0 a 100%
            uint8_t currentVolume = Sampler_GetFilteredPercentage();

            // Aplica a nova intensidade apenas ao LED selecionado no momento (Requisito 3.3)
            LedPwm_UpdateActiveLedIntensity(currentVolume);
        }

        // REQUISITO 3.6: Verifica se transcorreu o intervalo de 1 segundo (200 ciclos de 5ms)
        if (tickCounter >= dTICKS_PER_SECOND)
        {
            tickCounter = 0; // Reinicia o temporizador local da telemetria

            char txBuffer[dTX_BUFFER_SIZE];

            // Determina a string de estado de congelamento exigida pela especificacao
            const char *stateStr = (Button_IsSystemFrozen() == true) ? "OFF" : "ON";

            // Formata a string de saida seguindo rigorosamente o padrao exato exigido
            snprintf(txBuffer, dTX_BUFFER_SIZE,
                     "VALUE: %u%% || LED1: %u%% aceso || LED2: %u%% aceso || LED3: %u%% aceso || STATE: %s\r\n",
                     Sampler_GetFilteredPercentage(),
                     LedPwm_GetLedDuty(eBSP_LED_1),
                     LedPwm_GetLedDuty(eBSP_LED_2),
                     LedPwm_GetLedDuty(eBSP_LED_3),
                     stateStr);

            // Transmite os dados formatados via USART3
            Bsp_TransmitUartString(txBuffer);
        }
    }
  }
  /* USER CODE END 3 */
}

